# iteradores de incremento y de decremento

a = 4
# a = a + 3 Esta es la primera opción 
a += 3 # Esta es la opción más aconsejable

print(a)

# Lo mismo sucede con la resta
a = 4
a = a - 3  #Esta es la primera opción 
a -= 1 # Esta es la opción más aconsejable
print(a)

a = 10 
a *= 5
print(a)

a = 10 
a /= 5
print(a)